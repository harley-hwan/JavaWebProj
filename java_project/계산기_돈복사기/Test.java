package 계산기_돈복사기;

public class Test {
	// 메소드
	public void method () {
		System.out.println("나는 method()");
	}
	
	// Overloading
	public void method(int number) {
		System.out.println("나는 숫자를 받을 수 있는 method()");
	}

	public void method(String str) {
		System.out.println("나는 문자열을 받을 수 있는 method()");
	}
}
